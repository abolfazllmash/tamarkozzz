android {
  namespace = "com.example"
  compileSdk { version = release(36) { minorApiLevel = 1 } }

  defaultConfig {
    applicationId = "com.aistudio.avoidtheblocks.hqyzwp"
    minSdk = 24
    targetSdk = 36
    versionCode = 1
    versionName = "1.0"

    testInstrumentationRunner = "androidx.test.runner.AndroidJUnitRunner"
  }

  // امضای ریلیز فقط وقتی فعال می‌شود که فایل کی‌استور واقعاً موجود باشد
  // (مثلاً از طریق GitHub Secrets). در غیر این صورت با امضای دیباگ ساخته می‌شود
  // تا بیلد روی CI بدون نیاز به کلید هم کار کند.
  val releaseKeystorePath = System.getenv("KEYSTORE_PATH") ?: "${rootDir}/my-upload-key.jks"
  val hasReleaseKeystore = file(releaseKeystorePath).exists()

  signingConfigs {
    if (hasReleaseKeystore) {
      create("release") {
        storeFile = file(releaseKeystorePath)
        storePassword = System.getenv("STORE_PASSWORD")
        keyAlias = System.getenv("KEY_ALIAS") ?: "upload"
        keyPassword = System.getenv("KEY_PASSWORD")
      }
    }
  }

  buildTypes {
    release {
      isCrunchPngs = false
      isMinifyEnabled = false
      proguardFiles(getDefaultProguardFile("proguard-android-optimize.txt"), "proguard-rules.pro")
      // اگر کلید ریلیز نبود، با امضای دیباگ پیش‌فرض اندروید امضا می‌شود
      signingConfig = if (hasReleaseKeystore) signingConfigs.getByName("release")
                      else signingConfigs.getByName("debug")
    }
    debug {
      // از کی‌استور دیباگ پیش‌فرض اندروید استفاده می‌شود؛ نیازی به فایل دستی نیست
    }
  }
  compileOptions {
    sourceCompatibility = JavaVersion.VERSION_11
    targetCompatibility = JavaVersion.VERSION_11
  }
  buildFeatures {
    compose = true
    buildConfig = true
  }
  testOptions { unitTests { isIncludeAndroidResources = true } }
}

